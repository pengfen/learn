package com.test;
/**
 * 看程序写结果
 * 
 * 通过字符和一个整数相加 我们给出一张表 ASCII码表
 * 
 *    通过看完这张表以后 我们要记住三个值
 *    'a' 97
 *    'A' 65
 *    '0' 48
 *
 */
public class DataTypeDemo8 {
	public static void main(String[] args) {
		// 直接输出一个字符
		System.out.println('a');
		
		// 输出一个字符和一个整数做加法
		System.out.println('a' + 1); // 98
	}
}
