package com.demo;
/*
 * 流程控制语句 可以控制程序的执行流程
 * 
 * 分类
 *    顺序结构
 *    选择结构
 *    循环结构
 *    
 * 顺序结构  从上往下 依次执行
 */
public class SequenceDemo {
	public static void main(String[] args) {
		System.out.println("程序开始了");
		
		System.out.println("我爱Java");
		
		System.out.println("程序结束了");
		
		byte b = (byte) 300;
		System.out.println(b);
	}
}
